package day1;

public class Datatype {

	public static void main(String[] args) {
		int a=100;
		System.out.println("The initial value of a is");
		System.out.println(a);
		a=200;
		System.out.println("The updated value of a is");
		System.out.println(a);
		boolean g=true;
		System.out.println("boolean initial value is");
		System.out.println(g);
		g=false;
		System.out.println("boolean 1 updated value is ");
		System.out.println(g);
		char h='z';
		System.out.println("char initial  value is ");
		System.out.println(h);
		h='q';
		System.out.println("char updated   value is ");
	
	System.out.println(h);
	}

}
